// Register for joystick 0
#define JOYSTICK_REG_INPUT_0  0x278


#define JOYSTICK_MOVE_NOT  0xF // no movement with joystick
#define JOYSTICK_MOVE_LEFT  0xB // moving left with joystick
#define JOYSTICK_MOVE_RIGHT  0x7
#define JOYSTICK_MOVE_UP  0xE
#define JOYSTICK_MOVE_DOWN  0xD

#define JOYSTICK_MOVE_DOWN_LEFT  0x9
#define JOYSTICK_MOVE_DOWN_RIGHT  0x5
#define JOYSTICK_MOVE_UP_LEFT  0xA
#define JOYSTICK_MOVE_UP_RIGHT  0x6
